for (let i = 1; i <= 100; i++) {   // 1から100までの数に対して処理を行う
    let a = '';
    if (i % 3 == 0) {
        a += 'Fizz';   // 3の倍数なら、aに'Fizz'を追加
    }
    if (i % 5 == 0) {
        a += 'Buzz';   // 5の倍数なら、aに'Buzz'を追加
    }
    if (a === '') {   // iが3の倍数でも5の倍数でもない場合
        a = i;   // aにそのままの数値iをセット
    }
    console.log(a);  // 処理結果を表示
}
